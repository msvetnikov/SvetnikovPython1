# подсчитываем количество слов в текстовом файле

file_name = "text_file.txt"

f = open(file_name, "r")  # открываем файл для чтения
text = f.read()           # читаем весь текст
f.close()                 # закрываем файл

words = text.split()      # разбиваем текст на слова по пробелам и переносам
print("Количество слов:", len(words))
