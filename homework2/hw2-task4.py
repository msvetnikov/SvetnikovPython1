# сохраняем только уникальные строки из файла в новом порядке их первого появления

inp = "input.txt"             # исходный файл
out = "unique_output.txt"     # файл для результата

f = open(inp, "r")            # открыть исходный файл
lines = f.read().splitlines() # получить список строк без '\n'
f.close()                     # закрыть исходный файл

unique = []                   # здесь будут только уникальные строки
for line in lines:            # пройтись по всем строкам
    if line not in unique:    # если строки ещё нет в списке
        unique.append(line)   # добавить её

g = open(out, "w")            # открыть файл результата для записи
for line in unique:           # записать строки по одной
    g.write(line + "\n")
g.close()                     # закрыть файл результата

print("Готово: записано в", out)
